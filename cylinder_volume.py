import math
r = float(input("enter cylinder's radius : "))
h = float(input("enter cylinder's height: "))

volume = math.pi * (r**2) * h # volume of the cylinder is calculated by pi * r^2 * h

print("The volume of the cylinder is: ", (round((volume), 2))) # using round function to take the final answer with 2 decimal places as necessary for volume.




